DROP TABLE CART;
CREATE TABLE CART (
	cNO NUMBER (12) PRIMARY KEY,
	mID VARCHAR2 (20) REFERENCES MEMBERS (mID),
	pID VARCHAR (20) REFERENCES PRODUCT (pID),
	QTY NUMBER (3),
	oPRICE NUMBER (9)
);

-----------------------------------
DROP TABLE ORDERS;
CREATE TABLE ORDERS (
	oNO NUMBER (12) PRIMARY KEY,
	mID VARCHAR2 (10) REFERENCES MEMBERS (mID),
	oADDR VARCHAR2 (255),
	oTEL VARCHAR2 (30),
	oDATE DATE
);

---------------------------------------------
DROP TABLE ORDER_DETAIL;
CREATE TABLE ORDER_DETAIL (
	odNO NUMBER (12) PRIMARY KEY,
	oNO NUMBER (12) REFERENCES ORDERS (oNO),
	pID NUMBER (3) REFERENCES PRODUCT (pID),
	QTY NUMBER (3),
	oPRICE NUMBER (9)
);










































